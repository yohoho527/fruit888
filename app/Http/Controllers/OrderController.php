    //用自訂格式顯示訂單內容
    public function showContent(Order $order)
    {
        $result = "";
        $i = 1;
        foreach ($order->items as $item) {
            $result = $result . $item->title . $item->pivot->qty . "個";
            if($i != count($order->items)){
                $result = $result . ",";
            }
            $i += 1;
        }
        return $result;
    }
